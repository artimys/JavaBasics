package apps;

public class Loops {

	public static void main(String[] args) {
		
		// For loop
		for (int x = 1; x <= 10; x++) {
            System.out.println("x is " + x);
		}
		
		
		System.out.println("");
		
		// Do while loop
		int y = 5; 
		do {
			System.out.println("y is " + y);
			y++; 
		} while (y <= 10); 
		
		
		
		
		System.out.println("");
		
		
		// while loop
		 int z = 1; 
		 while (z <= 4) { 
			 System.out.println("x is " + z);
			 z++; 
	     } 
		
		
	}

}
